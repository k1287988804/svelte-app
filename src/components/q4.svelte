<script lang="ts">
	// Pick<T, K extends keyof T> 的作用是将某个类型中的子属性挑出来，变成包含这个类型部分属性的子类型。
	interface Todo {
		title: string;
		description: string;
		completed: boolean;
	}

	type TodoPreview = Pick<Todo, "title" | "completed">;

	const todo: TodoPreview = {
		title: "Clean room",
		completed: false
	};
</script>